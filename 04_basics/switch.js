const month = 3

switch (month){
    case 1: 
         console.log("this is january")    
        break;
    case 2: 
         console.log("this is feb")    
        break;
    case 3: 
         console.log("this is march")    
        break;
    case 4: 
         console.log("this is april")    
        break;
    case 5: 
         console.log("this is may")    
        break;

    default: console.log("default case match")
        break;
}